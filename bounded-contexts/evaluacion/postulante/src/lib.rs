//! # Evaluación Postulante
//!
//! **Tipo de Subdominio:** Soporte
//!
//! Este crate gestiona la información y el ciclo de vida de los postulantes
//! (candidatos) que participan en las evaluaciones. Apoya al subdominio core
//! pero no representa la ventaja competitiva principal.
//!
//! ## Conceptos de Dominio
//!
//! - **Postulante**: Persona que será evaluada mediante los exámenes
//! - **Datos personales**: Información del candidato (nombre, documento, etc.)
//!
//! ## Responsabilidades
//!
//! - CRUD de postulantes
//! - Validación de datos personales
//! - Gestión del estado del postulante en el sistema
